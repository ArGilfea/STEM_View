def EquationSHM(curveType:str, EquationType: str,parameters,parameters2,SHMType:str) -> str:
    """Gives a string of the specific equation for SHM"""
    if SHMType == "SHM":
        if EquationType == "Position":
            if curveType == "sin":
                return f"x(t) = {parameters[0]} sin({parameters[1]} t + {parameters[2]})"
            elif curveType == "cos":
                return f"x(t) = {parameters[0]} cos({parameters[1]} t + {parameters[2]})"
        elif EquationType == "Speed":
            if curveType == "sin":
                return f"v(t) = {parameters[0]*parameters[1]} cos({parameters[1]} t + {parameters[2]})"
            elif curveType == "cos":
                return f"v(t) = {-parameters[0]*parameters[1]} sin({parameters[1]} t + {parameters[2]})"
        elif EquationType == "Acceleration":
            if curveType == "sin":
                return f"a(t) = {-parameters[0]*parameters[1]*parameters[1]} sin({parameters[1]} t + {parameters[2]})"
            elif curveType == "cos":
                return f"a(t) = {-parameters[0]*parameters[1]} cos({parameters[1]} t + {parameters[2]})"    
    elif SHMType == "Damped":
        if EquationType == "Position":
            if curveType == "sin":
                return f"x(t) = {parameters[0]}" + f"e$^{{{-parameters[3]/(2*parameters2[0])}t}}$" + f"sin({parameters[4]:.2g} t + {parameters[2]})"
            elif curveType == "cos":
                return f"x(t) = {parameters[0]}" + f"e$^{{{-parameters[3]/(2*parameters2[0])}t}}$" + f"cos({parameters[4]:.2g} t + {parameters[2]})"
        elif EquationType == "Speed":
            if curveType == "sin":
                return ""
            elif curveType == "cos":
                return ""
        elif EquationType == "Acceleration":
            if curveType == "sin":
                return ""
            elif curveType == "cos":
                return ""   
        else:
            raise Exception("Invalid Motion Type ",SHMType)

def ODESHM(parameters,parameters2,SHMType:str) -> str:
    """Gives a string of the specific equation ODE SHM"""
    if SHMType == "SHM":
        return f"{parameters[0]}" + r"$\frac{d^2x}{dt^2}$" + f"= -{parameters[1]} x"
    elif SHMType == "Damped":
        return f"{parameters[0]}" + r"$\frac{d^2x}{dt^2}$" + f"= -{parameters[1]} x - {parameters2[3]}" + r"$\frac{dx}{dt}$"
    else:
        raise Exception("Invalid Motion Type ",SHMType)

def ODEDamped(parameters) -> str:
    """Gives a string of the specific equation ODE Damped"""
    return f"{parameters[0]}" + r"$\frac{d^2x}{dt^2}$" + f"= -{parameters[1]} x - {parameters[2]}" + r"$\frac{dx}{dt}$" 

def Equation2DWave(curveType:str,parameters,slice = 'full',cursor = 0,typeMovement: str = "Full"):
    """Gives a string of the equation of the 2D Wave"""
    if typeMovement == "Full":
        if slice == 'full':
            if curveType == "sin":
                return f"y(x,t) = {parameters[0]:.3g} sin({parameters[1]:.3g} x - {parameters[2]:.3g} t + {parameters[3]:.3g})"
            elif curveType == "cos":
                return f"y(x,t) = {parameters[0]:.3g} cos({parameters[1]:.3g} x - {parameters[2]:.3g} t + {parameters[3]:.3g})"
        elif slice == 'time':
            if curveType == "sin":
                return f"y(x = {cursor:.3g},t) = {parameters[0]:.3g} sin({parameters[1]:.3g} x - {(parameters[2] * cursor + parameters[3]):.3g})"
            elif curveType == "cos":
                return f"y(x = {cursor:.3g},t) = {parameters[0]:.3g} cos({parameters[1]:.3g} x - {(parameters[2] * cursor + parameters[3]):.3g})"
        elif slice == 'position':
            if curveType == "sin":
                return f"y(x,t = {cursor:.3g}) = {parameters[0]:.3g} sin({(parameters[1] * cursor + parameters[3]):.3g} - {parameters[2]:.3g} t)"
            elif curveType == "cos":
                return f"y(x,t = {cursor:.3g}) = {parameters[0]:.3g} cos({(parameters[1] * cursor + parameters[3]):.3g} - {parameters[2]:.3g} t)"
    elif typeMovement == "Standing":
        if slice == "full":
            return f"y(x,t) = {2 * parameters[0]:.3g} sin({parameters[1]:.3g} x) cos({parameters[2]:.3g} t)"
        elif slice == 'time':
            return f"y(x,t = {cursor:.3g}) = {2 * parameters[0]:.3g} sin({(parameters[1] * cursor)}) cos ({parameters[2]:.3g} t)"
        elif slice == 'position':
            return f"y(x,t = {cursor:.3g}) = {2 * parameters[0]:.3g} sin({(parameters[1])} x) cos ({parameters[2] * cursor:.3g})"

def PDE2DWave(parameters) -> str:
    """Gives a string of the specific equation PDE Wave 2D"""
    return f"{parameters[0]}" + r"$\frac{\partial^2y}{\partial t^2}$" + f"= {parameters[1]}" + r"$\frac{\partial^2y}{\partial x^2}$" 